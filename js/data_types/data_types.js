/*There are two types of data: primitive and reference.

Primitive data:

- number e.g. 5,
- string e.g. 'Car',
- boolean e.g. true or false,
- undefined,
- null,
- symbol.
*/

//Number

let firstNumber = 10;
console.log(firstNumber);

//String

let firstName = 'Eliza';
console.log(firstName);

//Boolean

let firstValue = true;
console.log(firstValue);

//Undefined

let firstUndefined = undefined;
console.log(firstUndefined);

//Null

let firstNull = null;
console.log(firstNull);


/*Reference data:

- objects,
- array.
*/

//Objects

let firstObject = {
  name: 'Eliza',
  surname: 'Taylor',
  age: 28
}
console.log(firstObject);

//Array

let firstArray = [2,3,4,5,6];
console.log(firstArray);
